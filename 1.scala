object variable 
{
    def main(args: Array[String]): Unit = {
    var i, j, k = 2
    var m, n = 5
    var f = 12.0f
    var g = 4.0f
    var c = 'X'

    val p = k + 12 * m
    val q = m / j
    val r = n % j
    val s = m / j * j
    val t = f + 10 * 5 + g
    i = i + 1
    val u = i * n

    println(s"k + 12 * m = $p")
    println(s"m / j = $q")
    println(s"n % j = $r")
    println(s"m / j * j = $s")
    println(s"f + 10 * 5 + g = $t")
    println(s"++i * n = $u")
  }
}


